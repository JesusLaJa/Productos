    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        // TODO add your handling code here:
        try {
            
            //Preparamos la llamada al SP
            CallableStatement myCall = con.prepareCall("{call USP_PRODUCTOS_UPDATE(?,?,?,?)}");
            //Asignamos el valor a los parametros 
            myCall.setString("PID", txtid.getText());
            myCall.setString("PDESCRIPCION", txtdescripcion.getText());
            myCall.setString("PPRECIO", txtprecio.getText());
            myCall.setString("PESTADO", txtestado.getText());
            
            //ejecutamos la llamada 
            myCall.executeUpdate();
            
            Integer id = myCall.getInt("PID");
            
            txtid.setText(id.toString());
            //Mostramos un mensaje en caso de ser exitosa la ejecucion
            JOptionPane.showMessageDialog(null, "Datos Actualizados con exito!");
            
            limpiar();
            mostrartabla("");


        } catch (Exception e) {
            //Mostramos el mesnaje de error en caso de que la ejecución no se haya realizado
            System.out.println("Error: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Error al actualizar los datos");
        }


    }//GEN-LAST:event_btnactualizarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        try {
            //Preparamos la llamada 
            CallableStatement myCall = con.prepareCall("{call USP_PRODUCTOS_INSERT(?,?,?,?)}");
            //Asignamos valor a los parametros
            myCall.setString("PDESCRIPCION", txtdescripcion.getText());
            myCall.setString("PPRECIO", txtprecio.getText());
            myCall.setString("PESTADO", txtestado.getText());
            //Registramos el parametro de salida
            myCall.registerOutParameter("PID", java.sql.Types.INTEGER);
            
            //Ejecutamos la llamada al SP
            myCall.execute();

            Integer id = myCall.getInt("PID");

            //int id = rs.getInt("PID");
            if (id > 0) {
                txtid.setText(id.toString());
                //Mostramos un mensaje en caso de ser exitosa la ejecucion
                JOptionPane.showMessageDialog(null, "Datos Guardados!");
            } else {
                JOptionPane.showMessageDialog(null, "Datos No Guardados!");
            }
            
            limpiar();
            mostrartabla("");

        } catch (Exception e) {
            //Mostramos mensaje de error en caso de que no se guarden los datos
            System.err.println("Ah ocurrido un error en el sistema" + e);
            JOptionPane.showMessageDialog(null, "Error al guardad los datos");

        }
    }//GEN-LAST:event_btnguardarActionPerformed

