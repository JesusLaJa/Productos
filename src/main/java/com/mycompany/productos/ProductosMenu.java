    public void mostrartabla(String nombreApellidos) throws SQLException {

        DefaultTableModel modelo = new DefaultTableModel();

        //añado el nombre de las columnas de la tabla
        modelo.addColumn("ID");
        modelo.addColumn("DESCRIPCION");
        modelo.addColumn("PRECIO");
        modelo.addColumn("ESTADO");

        //selecciono el modeloque es en donde se agregaran las columnas
        tabla.setModel(modelo);

        String datos[] = new String[4];

        try {
            //Se prepara la llamada al SP
            CallableStatement myCall = con.prepareCall("{call USP_PRODUCTOS_SELECT()}");

            //Ejecutamos la llamada al SP
            myCall.execute();

            //Agrego un arreglo que indique cuantos datos mostrara la tabla
            ResultSet rs = myCall.getResultSet();

            while (rs.next()) {
                //con el arreglo le asigno las posiciones a los datos de la tabla
                //e indicar el lugar al que corresponden en la base de datos
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);

                modelo.addRow(datos);
            }
            tabla.setModel(modelo);

        } catch (SQLException e) {
            //se muestra el mensaje de error 
            System.err.println("Error en el llamado de la tabla" + e);
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla");

        }

    }

